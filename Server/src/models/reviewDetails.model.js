import mongoose, { Schema } from "mongoose";
const revDetailSchema = new Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    message: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

export const allReviews = mongoose.model("revDetail", revDetailSchema);
